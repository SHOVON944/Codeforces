#include <iostream>
#include <string>

using namespace std;

void solve() {
    int n;
    string s;
    cin >> n >> s;


    int ones = 0;
    int zero = 0;
    for (char c : s) {
        if (c == '1') ones++;
        else zero++;
    }
    if (ones > zero) {
        cout << "YES\n";
        return;
    }

    else {
        cout << "NO\n";
        return ;
    }

        if (n == 1) {
        cout << (s[0] == '1' ? "YES\n" : "NO\n");
        return;
    }
}

int main() {
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}